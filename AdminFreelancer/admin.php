                    id_job: id,
                    type: "approve"
                }
                var me = $(this).closest("tr");
                $.ajax({
                    url: 'https://job.ahlupos.com/modules/job/api.php?ac=update_job',
                    data: data,
                    method: 'POST',
                    success: function(res) {
                        let a = JSON.parse(res);
                        if (a.code == 1) {
                            me.remove();
